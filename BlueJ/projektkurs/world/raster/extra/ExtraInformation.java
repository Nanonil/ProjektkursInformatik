package projektkurs.world.raster.extra;

/**
 * Tolle ExtraInformationen
 */
public class ExtraInformation {

	protected final int x, y;

	/**
	 * Konstruktor
	 */
	public ExtraInformation(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

}
